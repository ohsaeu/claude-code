create_label() {
  local name="$1"
  local color="$2"
  local description="$3"
  echo "  [Label] $name"
  gh label create "$name" --color "$color" --description "$description" $REPO_FLAG 2>/dev/null || true
}

create_milestone() {
  local title="$1"
  local due_date="$2"
  local description="$3"
  echo "  [Milestone] $title (due: $due_date)"
  gh api repos/$OWNER/$REPO/milestones \
    --method POST \
    --field title="$title" \
    --field due_on="${due_date}T00:00:00Z" \
    --field description="$description" \
    --silent 2>/dev/null || true
}

# 마일스톤 번호 조회
get_milestone_number() {
  local title="$1"
  gh api repos/$OWNER/$REPO/milestones --jq ".[] | select(.title == \"$title\") | .number" 2>/dev/null
}

create_issue() {
  local title="$1"
  local body="$2"
  local labels="$3"
  local milestone_title="$4"
  local assignee="$5"

  local args=()
  [ -n "$milestone_title" ] && args+=(--milestone "$milestone_title")
  [ -n "$assignee" ] && args+=(--assignee "$assignee")

  echo "  [Issue] $title"
  gh issue create \
    --title "$title" \
    --body "$body" \
    --label "$labels" \
    "${args[@]}" \
    --repo "$OWNER/$REPO"
}

